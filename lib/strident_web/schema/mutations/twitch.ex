defmodule StridentWeb.Schema.Mutations.Twitch do
  @moduledoc false

  use Absinthe.Schema.Notation
  alias StridentWeb.Resolvers.TwitchResolver

  input_object :twitch_login_input do
    @desc """
    A "random" string to be generated by the client.
    """
    field :code_verifier, non_null(:string)

    @desc """
    A Stride-only code generated by the backend when
    handling a redirect from Twitch in the native app flow.
    """
    field :internal_code, non_null(:string)

    field :locale, non_null(:string)
    field :timezone, non_null(:string)
  end

  object :twitch_mutations do
    @desc "Login with twitch to your user account"
    field :twitch_login, :login_result do
      arg(:input, non_null(:twitch_login_input))
      resolve(&TwitchResolver.twitch_login/3)
    end
  end
end
